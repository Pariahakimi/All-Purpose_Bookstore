
DROP TABLE WRITES_FOR;
DROP TABLE REVIEW;
DROP TABLE PURCHASED_BY;
DROP TABLE CUSTOMER;
DROP TABLE BOOK;
DROP TABLE AUTHOR;
DROP TABLE PUBLISHER;

-------------------------------------------------------
------   CREATE TABLE STATEMENTS    -------------------
-------------------------------------------------------

CREATE TABLE PUBLISHER(			
	P_name varchar(110) NOT NULL,
	P_url varchar (26),
	Primary Key(P_name)
);

CREATE TABLE AUTHOR (			
	lname varchar(70) NOT NULL,
	fname varchar(70) NOT NULL,
	phone char(10) NOT NULL,
	a_url varchar(26),
	Primary Key(lname, fname, phone)
);

CREATE TABLE BOOK (
	ISBN char(13) NOT NULL,
	Book_title varchar(160) NOT NULL,
	Price decimal(5,2) NOT NULL,
	alname varchar(70) NOT NULL,
	afname varchar (70) NOT NULL,
	aphone char (10),
	book_year int CHECK (book_year > 1814 AND book_year < 2017),
	Qty_stock int NOT NULL CHECK(Qty_stock > -1),
	Copies_sold int NOT NULL CHECK(copies_sold > -1),
	P_name varchar(110) NOT NULL,
	Primary Key(ISBN),
	Foreign Key(P_name) REFERENCES PUBLISHER(P_name),
	Foreign Key(alname, afname,aphone) REFERENCES AUTHOR(lname, fname, phone)
);

CREATE TABLE CUSTOMER (
	email varchar(30) NOT NULL,
	lname varchar(70) NOT NULL, 
	fname varchar(70) NOT NULL, 
	phone char(10),
	Primary Key(email)
);

CREATE TABLE PURCHASED_BY (
	c_email varchar(30) NOT NULL,
	B_ISBN char(13) NOT NULL,
	P_Date date NOT NULL, 
	Num_copies int NOT NULL,
	Primary Key(c_email, B_ISBN),
	Foreign Key (c_email) REFERENCES CUSTOMER(email),
	Foreign Key (B_ISBN) REFERENCES BOOK(ISBN)
);

CREATE TABLE REVIEW (
	Review_ID varchar(10) NOT NULL,
	c_email varchar(30) NOT NULL,
	B_ISBN char(13) NOT NULL,
	Num_stars int NOT NULL CHECK (Num_stars > -1 AND Num_stars < 6),
	Foreign Key (c_email) REFERENCES CUSTOMER(email),
	Foreign Key (B_ISBN) REFERENCES BOOK(ISBN)
);

CREATE TABLE WRITES_FOR(
	pname varchar(110) NOT NULL,
	a_lname varchar(70) NOT NULL,
	a_fname varchar(70) NOT NULL,
	a_phone char(10) NOT NULL,
	Primary Key(pname, a_lname,a_fname,a_phone),
	Foreign Key (pname) REFERENCES PUBLISHER(P_name), 
	Foreign Key (a_lname,a_fname,a_phone) REFERENCES AUTHOR(lname, fname, phone)	 
);

